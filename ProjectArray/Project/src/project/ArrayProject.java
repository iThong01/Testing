    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ID = Integer.parseInt(jTextField1.getText());
        int LOC = Search(ID);
        if (LOC != -1) {
            jLabel4.setText("ID : " + item[LOC].id);
            jTextField2.setText(String.valueOf(item[LOC].id));
            jTextField3.setText(String.valueOf(item[LOC].name));
            jTextField5.setText(String.valueOf(item[LOC].price));
            jTextField7.setText("");
            jTextField4.setText("");
            ImgShow(item[LOC].pic);
        }else{
            ImageIcon icon = new ImageIcon(getClass().getResource("/image/notFound.png"));
            Image img = icon.getImage().getScaledInstance(200, 200, Image.SCALE_SMOOTH);
            jLabel3.setIcon(new ImageIcon(img));
            jLabel4.setText("Not Found");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

