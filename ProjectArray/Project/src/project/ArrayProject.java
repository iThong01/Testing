    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //INSERTBUTTON
        if (jComboBox1.getSelectedIndex() == 0) {
            jLabel12.setText("Please Select type!");
        } else {
       int iId = Integer.parseInt(jTextField2.getText());
       for(int i=0;i<N;i++){
           if(item[i].id==iId){
                jTextField2.setText("EROR HAVE SAME ID!!!");
                return ;
            }
       }
       String iName = jTextField3.getText();
       String t = jComboBox1.getSelectedItem().toString();
       int iType;
        if(t.equals("BALL")) {  
            iType = 1;
        }else{
            iType = 2;
        }
       float iPrice = Float.parseFloat(jTextField5.getText());
       String iPic = jTextField7.getText();
       ITEM createItem = new ITEM(iId,iName,iType,iPrice,iPic);
       int index=Search(Integer.parseInt(jTextField4.getText()));
       if(index==-1 ){
           Insert(0,createItem);
       }else{
           Insert(index+1,createItem);
       }
       Show();
       ImgShow(iPic);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

