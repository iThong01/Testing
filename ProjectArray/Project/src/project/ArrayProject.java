   public int Search(int ITEM) {
        int LOC = -1;
        int i = 0;
        while (i < N) {
            if (item[i].id==(ITEM)) {
                LOC = i;
//                System.out.println(""+LOC);
                return LOC;
            } else {
                i++;
            }
        }
        return LOC;
    }
   public void Insert(int LOC,ITEM i) {
        int k = N - 1;
        while (k >= LOC) {
            item[k + 1] = item[k];
            k--;
        }
        item[LOC] = i;
        N++;
    }
public  void Delete(int K){
       for (int J=K;J<N;J++){
           item[J]=item[J+1];
       }
       N--;
    }
public void SortP(){
       for(int K=0; K<N-1;K++){ 
          int PTR=0;
          while(PTR<((N-1)-K)){
            if(item[PTR].price>item[PTR+1].price){
                ITEM temp1 =item[PTR]; 
                item[PTR]=item[PTR+1];
                item[PTR+1]=temp1;
            }
            PTR++; 
          }
       }
//       for(int i=0;i<N;i++) System.out.println(price[i]);             
    }
    public void Show(){
        for(int i =0;i<N;i++){
           jTable1.setValueAt(item[i].id,i,0);
           jTable1.setValueAt(item[i].name,i,1);
           String sType ;
           if(item[i].type==1){
               sType = "BALL";
           }else{
               sType = "ROCKET";
           }
           jTable1.setValueAt(sType,i,2);
           jTable1.setValueAt(item[i].price,i,3);
       }
    }
